  geom_polygon,
  trial_number,
  trial_name,
  contact_person,
  date_recorded,
  crop_type,
  crop_class,
  planting_date,
  previous_cutting,
  expected_harvest_date,
  residue_type,
  residue_management_method,
  residual_management_remarks,
  fertilizer_type,
  nutrient_application_date,
  application_rate,
  fertilizer_applications,
  foliar_sampling_date,
  herbicide_name,
  weed_application_date,
  weed_application_rate,
  herbicide_applications,
  pest_remarks,
  disease_remarks,
  harvest_date,
  harvest_yield,
  quality_remarks,
  well_known_text
from public.sugarcane_monitoring;

commit;
